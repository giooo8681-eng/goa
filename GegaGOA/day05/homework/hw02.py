#2)მომხარებელს შემოატანინეთ ორი რიცხვი, შემდეგ ამ 2 რიცხვს შორის ჩაატარეთ ყველა არითმეტიკული ოპერატორი (+, -, *, /, **)

num1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = int(input("შეიყვანეთ მეორე რიცხვი: "))
print("ჯამი: " + str(num1 + num2))
print("სხვაობა: " + str(num1 - num2))
print("ნამრავლი: " + str(num1 * num2))
print("გაყოფა " + str(num1 / num2))
print("მოცემული რიცხვების ხარისხი: " + str(num1 ** num2))   

